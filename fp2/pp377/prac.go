// lucasVPrac computes V_e(alpha) using a PRAC differential addition chain
// (Montgomery 1992) with swaps fused into op codes and pointer-based register
// rotation. Case 3 is fast-pathed to avoid the switch.
func lucasVPrac(alpha *fp.Element) fp.Element {
	var reg [5]fp.Element
	var two fp.Element
	two.SetUint64(2)

	// Specialize ops[0..6]: the initial (3, 10) × 3 + 3 pattern.
	// Each case-3 after case-10 has A=B, so T = A·B - C = A² - C
	// uses Square instead of Mul (saves 0.2 Mfp each, 0.8 Mfp total).
	//
	// Op 0 (case 3):  A=B=α, C=2.  T = α²-2.     → A=α, B=α²-2, C=α.
	// Op 1 (case 10): A=A·B-C = α³-3α. B=A, C=2.  → A=B=V₃, C=2.
	// Op 2 (case 3):  T = V₃²-2.                   → A=V₃, B=V₃²-2, C=V₃.
	// Op 3 (case 10): A=A·B-C. B=A, C=2.           → A=B=V₉, C=2.
	// Op 4 (case 3):  T = V₉²-2.                   → A=V₉, B=V₉²-2, C=V₉.
	// Op 5 (case 10): A=A·B-C. B=A, C=2.           → A=B=V₂₇, C=2.
	// Op 6 (case 3):  T = V₂₇²-2.                  → A=V₂₇, B=V₂₇²-2, C=V₂₇.
	reg[0].Square(alpha).Sub(&reg[0], &two)          // V₂ = α²-2
	reg[0].Mul(&reg[0], alpha).Sub(&reg[0], alpha)   // V₃ = α·V₂ - α
	reg[1].Square(&reg[0]).Sub(&reg[1], &two)        // V₃²-2
	reg[3].Mul(&reg[0], &reg[1]).Sub(&reg[3], &reg[0]) // V₉ = V₃·(V₃²-2) - V₃
	reg[1].Square(&reg[3]).Sub(&reg[1], &two)        // V₉²-2
	reg[4].Mul(&reg[3], &reg[1]).Sub(&reg[4], &reg[3]) // V₂₇ = V₉·(V₉²-2) - V₉
	reg[1].Square(&reg[4]).Sub(&reg[1], &two)        // V₂₇²-2
	reg[0].Set(&reg[4])                              // A = V₂₇
	reg[2].Set(&reg[4])                              // C = V₂₇

	pA, pB, pC, pT := &reg[0], &reg[1], &reg[2], &reg[3]

	for _, fop := range pracOps[7:] {
		if fop&0x80 != 0 {
			pA, pB = pB, pA
		}
		switch fop & 0x0f {
		case 3:
			pT.Mul(pA, pB).Sub(pT, pC)
			pC, pB, pT = pB, pT, pC
		case 1:
			var T2 fp.Element
			pT.Mul(pA, pB).Sub(pT, pC)
			T2.Mul(pT, pA).Sub(&T2, pB)
			pB.Mul(pT, pB).Sub(pB, pA)
			pA.Set(&T2)
		case 2, 4:
			pT.Mul(pA, pB).Sub(pT, pC)
			pA.Square(pA).Sub(pA, &two)
			pB, pT = pT, pB
		case 5:
			pT.Mul(pA, pC).Sub(pT, pB)
			pA.Square(pA).Sub(pA, &two)
			pC, pT = pT, pC
		case 6:
			var T2, T3 fp.Element
			pT.Mul(pA, pB).Sub(pT, pC)
			T2.Square(pA).Sub(&T2, &two)
			T3.Mul(&T2, pA).Sub(&T3, pA)
			pA.Set(&T3)
			T3.Mul(&T2, pT).Sub(&T3, pC)
			pC.Set(pB)
			pB.Set(&T3)
		case 7:
			var T2, T3 fp.Element
			pT.Mul(pA, pB).Sub(pT, pC)
			T2.Square(pA).Sub(&T2, &two)
			T3.Mul(&T2, pA).Sub(&T3, pA)
			T2.Mul(pT, pA).Sub(&T2, pB)
			pA.Set(&T3)
			pB.Set(&T2)
		case 8:
			var T2, T3 fp.Element
			pT.Mul(pA, pB).Sub(pT, pC)
			T2.Square(pA).Sub(&T2, &two)
			T3.Mul(&T2, pA).Sub(&T3, pA)
			pC.Mul(pA, pC).Sub(pC, pB)
			pA.Set(&T3)
			pB.Set(pT)
		case 9:
			pT.Mul(pC, pB).Sub(pT, pA)
			pB.Square(pB).Sub(pB, &two)
			pC, pT = pT, pC
		case 10:
			pA.Mul(pA, pB).Sub(pA, pC)
			pB.Set(pA)
			pC.Set(&two)
		}
	}

	return *pA
}
