// Package main generates cbrt.go, cbrt_addchain.go, prac.go, and cbrt_test.go
// for each Fp2 field from a single set of templates, deduplicating the
// algorithmic framework.
//
// Usage:
//
//	go run ./internal/generator/cbrt/
package main

